	public function dashboard()
	{
		return view('admin.dashboard');
	}

	public function main()
	{
		$users = User::paginate(25);
		$users->setPath('');
		return view('admin.main')->with('users', $users);
	}

	public function create()
	{
		$validate = Validator::make(Input::all(), array(
			'username' 	=> 'required||unique:users',
			'nama' 	=> 'required||min:3',
			'password' 		=> 'required||min:5',
			'password2' => 'same:password',
			'email'	=> 'required||min:3',
			'jk'	=> 'required',
			));

		if ($validate -> fails()){
			$validate = Validator::make(Input::all(), array(
				'username' 	=> 'required||unique:users',
				'nama' 	=> 'required||min:3',
				'password' 		=> 'required||min:5',
				'password2' => 'same:password',
				'email'	=> 'required||min:3',
				'jk'	=> 'required',
				'create' => 'required',
				));
			return redirect('admin')->withErrors($validate)->withInput();
		}
		else{		
	    //
			$user = new User();
			$user->username = Input::get('username');
			$user->nama_pasien = Input::get('nama');
			$user->password = Hash::make(Input::get('password'));
			$user->email = Input::get('email');
			$user->jk = Input::get('jk');

			if (Input::has('ttl'))
				$user->ttl = Input::get('ttl');
			else				
				$user->ttl = '';

			if (Input::has('alamat'))
				$user->alamat = Input::get('alamat');
			else				
				$user->alamat = '';

			if (Input::has('hp1'))
				$user->hp1 = Input::get('hp1');
			else				
				$user->hp1 = '';

			if (Input::has('hp2'))
				$user->hp2 = Input::get('hp2');
			else				
				$user->hp2 = '';


			$user->role = 'admin';
			$user->save();
			return redirect('admin');
		}
	}

}
